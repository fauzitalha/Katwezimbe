                lblTranAmount.Text = SHR_TRAN.XXX_AMOUNT_RECEIVED_RETURNED;
                lblTranStatus.Text = SHR_TRAN.APPROVAL_STAGE;
            }
            catch (Exception mm)
            {
                DisplayAlert("Error 02", mm.Message, "OK");
            }
            
        }
        #endregion

    }
